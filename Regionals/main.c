#include "Subsystems/drivetrain.h"
#include "Subsystems/arm.h"
#include "Subsystems/veinGrabber.h"

task main()
{
	bool clawToggle = false;
	bool curClaw = false;
	bool prevClaw = false;

	bool dualStick = true;

  while (true)
  {

  	if (vexRT[Btn8U]) { // Vein Score
      setArmSetpoint(1085);
      openJoint();
    } else if (vexRT[Btn8L]) { // Vein Pickup
      setArmSetpoint(795);
      closeJoint();
      openClaw();
    } else if (vexRT[Btn8R]) { // High Artery
      setArmSetpoint(1800);
    } else if (vexRT[Btn8D]) { // Low Artery
      setArmSetpoint(1650);
    }

   updateArm(vexRT[Btn6U], vexRT[Btn6D]); // Update the arm position using the PID controller




   prevClaw = curClaw;
    if (vexRT[Btn5D]) {
    	curClaw = true;
    } else {
    	curClaw = false;
    }


    // CLAW
    // Rising edge detector
		if (curClaw && !prevClaw) {
		    // This will set intakeToggle to true if it was previously false
		    // and intakeToggle to false if it was previously true,
		    // providing a toggling behavior.
		    clawToggle = !clawToggle;
		}

		// Using the toggle variable to control the robot.
		if (clawToggle) {
		    closeClaw();
		}
		else {
		    openClaw();
		}


  	// Drive
    if (vexRT[Btn7L]) {
    	dualStick = false;
    } else if (vexRT[Btn7R]) {
    	dualStick = true;
    }

    if (dualStick) {
    	arcadeDrive(vexRT[Ch3], vexRT[Ch1], vexRT[Btn5U]);
    } else {
    	arcadeDrive(vexRT[Ch3], vexRT[Ch4], vexRT[Btn5U]);
    }


    if (vexRT[Btn7U]) {
    	lineFollowing(vexRT[Btn7D]);
    }



  }
}
